void main() {
  var first = 10;
  var second = 3;

/**
 * Berikut adalah beberapa operator aritmatika yang dapat digunakan : 
 * + untuk penjumlahan
 * - untuk pengurangan
 * * untuk perkalian
 * / untuk pembagian
 * % untuk modulus
 * ~/ untuk pembagian bulat (yang menghasilkan bilangan bulat)
 */
  print(first + second);
  print(first - second);
  print(first * second);
  print(first / second);
  print(first ~/ second);
  print(first % second);

}
